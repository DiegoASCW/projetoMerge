public class Usuarios {

    private String nome;
    private int idade;
    private String cpf;
    private float altura;
    private int telefone;

    public Usuarios(String nome, int idade, String cpf, float altura, int telefone){
        this.nome = nome;
        this.idade = idade;
        this.cpf = cpf;
        this.altura = altura;
        this.telefone = telefone;
    }

}
